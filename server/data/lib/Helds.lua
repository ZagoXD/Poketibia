			local Tier = getItemAttribute(ball.uid, "heldy")
			if Tier and Tier > 0 and Tier < 8 then
				if canRegen(cid, sid) then
					if isCreature(sid) and getCreatureHealth(sid) < getCreatureMaxHealth(sid) then
						addEvent(function() if isCreature(sid) then doCreatureAddHealth(sid,bonus) doSendAnimatedText(getThingPos(sid), "+ "..bonus, 30) onPokeHealthChange(cid) end end, 0)
					end
					addEvent(doRegenerateWithY, 1000, cid, sid)  
				else
					addEvent(doRegenerateWithY, 1000, cid, sid)
				end
			end   
		end
	end 
end

