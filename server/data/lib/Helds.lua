if ftier == '' or ftier == 0 or ftier > 7 then return {} end
if ttier == '' or ttier == 0 or ttier > 7 then return {} end
if type == '' then type = "xy" end

if types[type] == "xy" then
local hft = {}
for tiers = ftier, ttier do
for held = 1, #xheldTable do
if xheldTable[held].iid ~= 0 then
if xheldTable[held].tier == tiers then
table.insert(hft, xheldTable[held])
elseif xheldTable[held].tier == tiers then
table.insert(hft, xheldTable[held])
end
end
end
for held = 1, #yheldTable do
if yheldTable[held].iid ~= 0 then 
if yheldTable[held].tier == tiers then
table.insert(hft, yheldTable[held])
elseif yheldTable[held].tier == tiers then
table.insert(hft, yheldTable[held])
end
end
end
end
return hft
else--
local heldtable = type == "y" and yheldTable or type == "x" and xheldTable
local hftier = {}
for tiers = ftier, ttier do
for held = 1, #heldtable do
if heldtable[held].tier == tiers then
table.insert(hftier, heldtable[held])
elseif heldtable[held].tier == tiers then
table.insert(hftier, heldtable[held])
end
end
end
return hftier
end
end

function getHeldsInContainer(container)
if not isContainer(container) then return {} end
local items = {}
local helds = {}
local allhelds = getHeldsAllFromTier("xy", 1, 7)
for z=1, #allhelds do
table.insert(helds, allhelds[z].iid)
end
	if isContainer(container) and getContainerSize(container) > 0 then
		for slot=0, (getContainerSize(container)-1) do
			local item = getContainerItem(container, slot)
				if isContainer(item.uid) then
					local itemsbag = getHeldsInContainer(item.uid)
					for i=0, #itemsbag do
						table.insert(items, itemsbag[i])
					end
				elseif isInArray(helds, item.itemid) then
					table.insert(items, item)
				end
		end
	end
	return items
end

